// Regenerate src/templates/registry.ts from every src/templates/<id>/meta.json
// that has a matching adapter.ts + resume.typ. Run after templates are added:
//   node scripts/build-registry.mjs
import fs from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const TEMPLATES_DIR = path.join(ROOT, "src", "templates");
const OUT = path.join(TEMPLATES_DIR, "registry.ts");

// Curated ordering first; everything else alphabetical by display name.
const PRIORITY = { vantage: 0, swe: 1, "basic-resume": 2 };

const entries = [];
for (const id of fs.readdirSync(TEMPLATES_DIR)) {
  const dir = path.join(TEMPLATES_DIR, id);
  if (!fs.statSync(dir).isDirectory()) continue;
  const metaPath = path.join(dir, "meta.json");
  const adapterPath = path.join(dir, "adapter.ts");
  const typPath = path.join(dir, "resume.typ");
  if (!fs.existsSync(metaPath) || !fs.existsSync(adapterPath) || !fs.existsSync(typPath)) continue;
  let meta;
  try {
    meta = JSON.parse(fs.readFileSync(metaPath, "utf8"));
  } catch (e) {
    console.warn(`skip ${id}: bad meta.json (${e.message})`);
    continue;
  }
  if (meta.id !== id) {
    console.warn(`skip ${id}: meta.id "${meta.id}" != dir name`);
    continue;
  }
  entries.push(meta);
}

entries.sort((a, b) => {
  const pa = PRIORITY[a.id] ?? 100;
  const pb = PRIORITY[b.id] ?? 100;
  if (pa !== pb) return pa - pb;
  return (a.name || a.id).localeCompare(b.name || b.id);
});

const alias = (id) => "a_" + id.replace(/[^a-zA-Z0-9]/g, "_");
const esc = (s) => String(s ?? "").replace(/\\/g, "\\\\").replace(/"/g, '\\"');

const imports = entries.map((m) => `import { adapt as ${alias(m.id)} } from "./${m.id}/adapter";`).join("\n");

const defs = entries
  .map((m) => {
    const pkg = m.pkg ? `\n    pkg: "@preview/${esc(m.pkg)}",` : "";
    return `  {
    id: "${esc(m.id)}",
    name: "${esc(m.name)}",
    description: "${esc(m.description)}",
    accent: "${esc(m.accent || "#333333")}",${pkg}
    adapt: ${alias(m.id)},
  },`;
  })
  .join("\n");

const out = `import type { Resume } from "@/lib/resume/schema";
${imports}

/**
 * Single source of truth for resume templates. GENERATED by
 * scripts/build-registry.mjs from each src/templates/<id>/meta.json — do not
 * edit by hand; add a template dir (adapter.ts + resume.typ + meta.json) and
 * rerun the script. Each entry pairs UI metadata with the pure adapter that
 * maps a Resume into the JSON its resume.typ consumes. Most templates wrap a
 * Typst Universe package (\`pkg\`), auto-downloaded into the Typst cache.
 */
export interface TemplateDef {
  id: string;
  name: string;
  description: string;
  /** Swatch/accent color for the picker. */
  accent: string;
  /** Provenance: the @preview package this template renders with, if any. */
  pkg?: string;
  adapt: (r: Resume) => unknown;
}

export const TEMPLATE_DEFS: TemplateDef[] = [
${defs}
];

export const DEFAULT_TEMPLATE_ID = "vantage";

export const TEMPLATE_IDS: string[] = TEMPLATE_DEFS.map((t) => t.id);

export function getTemplateDef(id: string): TemplateDef {
  return TEMPLATE_DEFS.find((t) => t.id === id) ?? TEMPLATE_DEFS[0];
}

export function isTemplateId(id: unknown): id is string {
  return typeof id === "string" && TEMPLATE_IDS.includes(id);
}
`;

fs.writeFileSync(OUT, out);
console.log(`Wrote ${OUT} with ${entries.length} templates:`);
console.log(entries.map((m) => `  - ${m.id} (${m.name})`).join("\n"));
